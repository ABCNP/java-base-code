public class ArithmeticOperatorDemo02 {
    public static void main(String[] args) {
        int a = 18;
        System.out.printf("-a为%d%n", -a);

        System.out.printf("++a为%d%n", ++a);
        System.out.printf("a++为%d%n", a++);
        System.out.printf("a为%d%n", a);
    }
}
/*
    -:取反
    ++:自增
    --:自减
*/